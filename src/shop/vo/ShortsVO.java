package shop.vo;

public class ShortsVO {
	private int shorts_no;
	private String shorts_title;
	private String shorts_content;
	private String shorts_url;
	private String prod_id;
	
	public int getShorts_no() {
		return shorts_no;
	}
	public void setShorts_no(int shorts_no) {
		this.shorts_no = shorts_no;
	}
	public String getShorts_title() {
		return shorts_title;
	}
	public void setShorts_title(String shorts_title) {
		this.shorts_title = shorts_title;
	}
	public String getShorts_content() {
		return shorts_content;
	}
	public void setShorts_content(String shorts_content) {
		this.shorts_content = shorts_content;
	}
	public String getShorts_url() {
		return shorts_url;
	}
	public void setShorts_url(String shorts_url) {
		this.shorts_url = shorts_url;
	}
	public String getProd_id() {
		return prod_id;
	}
	public void setProd_id(String prod_id) {
		this.prod_id = prod_id;
	}
}
